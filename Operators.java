class Operators {
    public static void main(String[] args ){
        int number1 = 18;
        int number2 = 3;

        // addition
        System.out.println(number1 + number2); // 21

        // substraction
        System.out.println(number1 - number2); // 15

        // multiplication
        System.out.println(number1 * number2); // 54

        // division
        System.out.println(number1 / number2); // 6

        // remainder or modulus or modulo
        System.out.println(number1 % number2); // 0
    }
}